    private void jButtonCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalculateActionPerformed
        NumberFormat numberFormat = NumberFormat.getCurrencyInstance();
        double amount;
        double rate;
        int term;
        
        if(Validator.isTextFieldDoubleInRange(jTextFieldAmount, 0, 1000000, "Amount")
            && Validator.isTextFieldDoubleInRange(jTextFieldRate, 0, 1, "Rate")
            && Validator.isTextFieldDoubleInRange(jTextFieldTerm, 0, 10000, "Term in months"))
        {
            amount = Double.parseDouble(jTextFieldAmount.getText());
            rate = Double.parseDouble(jTextFieldRate.getText());
            term = Integer.parseInt(jTextFieldTerm.getText());
            if(jRadioButtonPV.isSelected())
            {
                financial = new PresentValue(amount, rate, term);
            }
            if(jRadioButtonLoan.isSelected())
            {
                financial = new Loan(amount, rate, term);
            }
            if(jRadioButtonAnnuity.isSelected())
            {
                financial = new Annuity(amount, rate, term);
            }
        jTextFieldResult.setText(numberFormat.format(financial.getResult()));
        }
        jButtonSchedule.setEnabled(true);
    }//GEN-LAST:event_jButtonCalculateActionPerformed

