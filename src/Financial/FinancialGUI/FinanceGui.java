    private void jButtonScheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonScheduleActionPerformed
        //Formats numbers to currencies and percentages for the jTable
        NumberFormat currency = NumberFormat.getCurrencyInstance();
        NumberFormat percent = NumberFormat.getPercentInstance();
        
        percent.setMaximumFractionDigits(3);
        
        //set variable names for the table and table model
        JTable schedule;
        DefaultTableModel model;
        
        if (financial instanceof PresentValue || financial instanceof Loan || financial instanceof Annuity)
        {
            String[] columns={"Month", financial.getFirstBalDesc(),financial.getInterestDesc(),
                Financial.RATEDESC, financial.getLastBalDesc()};
            String[][] table = new String[financial.getTerm()][5];
            
            model = new DefaultTableModel(table, columns);
            schedule = new JTable(model);
            
            for(int i=0;i<financial.getTerm();i++)
            {
                schedule.setValueAt(i+1, i, 0);//month
                schedule.setValueAt(currency.format(financial.getFirstBalance(i+1)), i, 1);//initial balance
                schedule.setValueAt(currency.format(financial.getIntFactor(i)), i, 2);//principle factor
                schedule.setValueAt(percent.format(financial.getRate()), i, 3);//payment per month
                schedule.setValueAt(currency.format(financial.getLastBalance(i+1)), i, 4);//final balance
            }
            
            //create table display container
            JScrollPane scroller = new JScrollPane();
            JDialog dialog = new JDialog();

            dialog.add(scroller);
            dialog.add(schedule);
            dialog.setTitle(financial.getTitle());
            dialog.pack();
            dialog.setVisible(true);
            dialog.setLocationRelativeTo(this);
            
        }
       
    }//GEN-LAST:event_jButtonScheduleActionPerformed

